function setLanguage(lang) {
    document.documentElement.lang = lang;
    localStorage.setItem('preferred-language', lang);
    
    // Update the language toggle button text
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        const langMap = {
            'en': 'EN 🇬🇧',
            'ru': 'RU 🇷🇺',
            'kz': 'KZ 🇰🇿'
        };
        langToggle.textContent = langMap[lang] || 'EN 🇬🇧';
    }
    
    // Apply translations
    const currentTranslations = translations[lang] || translations['en'];
    
    // Update elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (currentTranslations[key]) {
            element.textContent = currentTranslations[key];
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-placeholder-translate]').forEach(element => {
        const key = element.getAttribute('data-placeholder-translate');
        if (currentTranslations[key]) {
            element.placeholder = currentTranslations[key];
        }
    });
    
    // Close the dropdown
    const navDropdown = document.querySelector('.nav-dropdown:last-child');
    if (navDropdown) {
        navDropdown.classList.remove('active');
    }
    
    console.log('Language set to:', lang);
}

